function ryry_goto()
	g_AskClientStringEx(GetStringTask(TASK_S_POSITION), 0, 256, "T×m tªn b¶n ®å (ko dÊu viÕt th­êng)", {ryry_searchMap, self})
end
-- Sau khi nguoi dung dien ten map
function ryry_searchMap(input)

	if input == "1" or input == "" then
		g_AskClientStringEx(GetStringTask(TASK_S_POSITION), 0, 256, "Map X Y Fight", {EnterPosition2, self})	
		return 1
	end

	local szTitle = "KÕt qu¶ t×m (nÕu cã):"
	local tbOpt = {}
	firstFound = 1
	for i=1, getn(tbMapList) do
		local mapInfo = tbMapList[i]
		if strfind(mapInfo[1],input) then
			firstFound = i
			tinsert(tbOpt, format("%s/#ryry_cbDiMap(%d)", mapInfo[3], i))  --{tbEquip[i][1], laydoxanh2, {i, nType}}	
		else
			--Msg2SubWorld("<color=red>"..mapInfo[1].."<color><color=yellow>"..input.."<color>")
		end		
	end

	tinsert(tbOpt, {"Quay l¹i",ryry_goto})
	tinsert(tbOpt, {"Rêi khái."})

	-- khong tim thay ?
	local foundN = getn(tbOpt)

	if foundN == 0 then
		ryry_goto()
	elseif foundN == 1 then
		ryry_cbDiMap(firstFound)
	else
		Say(szTitle, foundN, tbOpt)
	end
end
-- Sau khi nguoi choi chon map
function ryry_cbDiMap(mapIndex)
	g_AskClientStringEx(GetStringTask(TASK_S_POSITION), 0, 256, "To¹ ®é", {ryry_diMap, {mapIndex}})
end
-- Sau khi nguoi choi chon toa do
function ryry_diMap(mapIndex, input)
	local mapInfo = tbMapList[mapIndex]
	ryry_enterPosition(mapInfo[2].." "..input.." 1")
end

function ryry_enterPosition(szPos)

	Msg2SubWorld("<color=red>GO2"..szPos.."<color>")


	local tbPos = split(szPos, " ")
	local nMap = tonumber(tbPos[1])
	local nPosX = tonumber(tbPos[2])
	local nPosY = tonumber(tbPos[3])
	local isFight = tonumber(tbPos[4])
	
	if nPosY == nil and isFight == nil then
		local W,X,Y = GetWorldPos()
		nPosY = nPosX
		nPosX = nMap
		nMap = W
		isFight = 2
	end
	
	local nMove;
	if nPosX < 1000 and nPosY < 1000 then
		nPosX = nPosX*8
		nPosY = nPosY*16
	end
	
	nMove = NewWorld(nMap,nPosX, nPosY)
	AddSkillState(963, 1, 0, 18*3)
	if isFight == 1 then
		SetFightState(1)
	end
	
	if isFight == 0 then
		SetFightState(0)
	end

	if nMove ~= 1 then
		GMMsg2Player("DÞch chuyÓn ®Õn vÞ trÝ","ThÊt b¹i! VÞ trÝ kh«ng hîp lÖ, vui lßng kiÓm tra l¹i.")
